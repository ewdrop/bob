package com.wwj.lianxi;

public class Student {
    String name;
    int age;
    double height;
    boolean isSchooling;

    public Student() {
    }

    public Student(String name, int age, double height, boolean isSchooling) {
        this.name = name;
        this.age = age;
        this.height = height;
        this.isSchooling = isSchooling;
    }
}
